
public class UnaryExample {

	public static void main(String[] args) {
		
		//증감연산자 ++, --
		int i = 1;
		int j = i++; //후위연산자
		
		System.out.println(i);
		System.out.println(j);
		System.out.println();
		
		int x=1;
		int y = ++x; //전위연산자
		System.out.println(x);
		System.out.println(y);
		
		
		

	}

}
