public class CommentsExample {
	
	/* 메인함수(메서드)는 프로그램을 실행하기 위해 반드시 작성해야 하는 함수(메서드)입니다
	  
	  main함수 블록({})안에 들어있는 코드의 명령들을 실행
	 
	  main함수를 쉽게 작성하시려면 이클립스 코드작성부에 
	  'main'이라고 입력하시고  Ctrl + Space를 누르면 자동완성
	 */
	public static void main(String[] args) {
		//코드 실행 단축키 컨트롤 F11
		System.out.print("안녕~~!\n");
		System.out.print("잘가~~!\n");
		
		/*
		 * println()은 print()와 달리 출력 이후에 자동으로 줄개행
		 */
		
		System.out.println("메롱~~!");
		System.out.println("빠이~~!");
		//sysout 이라고 적고 컨트롤 스페이스 누르면 자동 생성
		
		System.out.println("방가방가!!");
		
		System.out.printf("%d + %d = %d \n ",10,3,13);
	}
}
